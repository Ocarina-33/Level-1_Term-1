#include <stdio.h>
int main()
{
    double n, tax;
    scanf("%lf", &n);
    if (n < 0)
    {
        printf("Error!");
        return 0;
    }
    if (n > 0 && n <= 350000)
    {
        tax = 0;
    }
    if (n > 350000 && n <= 450000)
    {
        tax = (n - 350000) * 0.05;
    }

    if (n > 450000 && n <= 750000)
    {
        tax = 100000 * 0.05 + (n - 450000) * 0.1;
    }
    if (n > 750000 && n <= 1150000)
    {
        tax = 100000 * 0.05 + 300000 * 0.1 + (n - 750000) * 0.15;
    }
    if (n > 1150000 && n <= 1650000)
    {
        tax = 100000 * 0.05 + 300000 * 0.1 + 400000 * 0.15 + (n - 1150000) * 0.2;
    }
    if (n > 1650000)
    {
        tax = 100000 * 0.05 + 300000 * 0.1 + 400000 * 0.15 + 500000 * 0.2 + (n - 1650000) * 0.25;
    }

    printf("%lf", tax);
    return 0;
}